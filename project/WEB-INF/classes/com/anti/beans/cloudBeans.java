package com.anti.beans;

public class cloudBeans {

	
	int id;
	
	String cloud_name;
	
	String cloud_password;

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public String getCloud_name() {
		return cloud_name;
	}

	public void setCloud_name(String cloud_name) {
		this.cloud_name = cloud_name;
	}

	public String getCloud_password() {
		return cloud_password;
	}

	public void setCloud_password(String cloud_password) {
		this.cloud_password = cloud_password;
	}
	
	
	
}
